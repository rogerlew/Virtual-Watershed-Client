    void drawParticles()
    {
        int resolution = swe.Count;

        points = new ParticleSystem.Particle[resolution];

        int i = 0;
        foreach(KeyValuePair<int, Color> entry in swe)
        {
            Color c = entry.Value;
            c.a = c.a * 0.4f;
            float x = availableWidth * ((float)entry.Key / timeSlider.maxValue);
            points[i].position = new Vector3(x, 0f, 0f);
            points[i].color = c;
            points[i].size = 0.5f;

            i++;
        }

// Used to calibrate the scale
//        points[resolution - 1].position = new Vector3(availableWidth, 0f, 0f);
//        points[resolution - 1].size = 10f;

        GetComponent<ParticleSystem>().SetParticles(points, points.Length);
